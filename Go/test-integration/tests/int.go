func main() {
  var a = 42;
  var b = 17;

  println(+a);     // should print "42"
  println(-a);     // should print "-42"
  println(a == b); // should print "false"
  println(a != b); // should print "true"
  println(a < b);  // should print "false"
  println(a <= b); // should print "false"
  println(a > b);  // should print "true"
  println(a >= b); // should print "true"
  println(a + b);  // should print "59"
  println(a - b);  // should print "25"
  println(a * b);  // should print "714"
  println(a / b);  // should print "2"
  println(a % b);  // should print "8"
}
